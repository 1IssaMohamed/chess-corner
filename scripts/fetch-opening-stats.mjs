// Fetches opening win/draw/loss stats from Lichess explorer API.
// Run with: node scripts/fetch-opening-stats.mjs
// Writes src/data/openingStats.ts — commit the output.

import { writeFileSync } from "fs";

// UCI moves that define each opening's characteristic position
const OPENINGS = [
  { id: "ruy-lopez", play: "e2e4,e7e5,g1f3,b8c6,f1b5" },
  { id: "italian", play: "e2e4,e7e5,g1f3,b8c6,f1c4" },
  { id: "queens-gambit", play: "d2d4,d7d5,c2c4" },
  { id: "london-system", play: "d2d4,d7d5,g1f3,g8f6,c1f4" },
  { id: "ponziani", play: "e2e4,e7e5,g1f3,b8c6,c2c3" },
  { id: "sicilian-defense", play: "e2e4,c7c5" },
  { id: "caro-kann", play: "e2e4,c7c6" },
  { id: "french-defense", play: "e2e4,e7e6" },
  { id: "kings-indian", play: "d2d4,g8f6,c2c4,g7g6,b1c3,f8g7,e2e4,d7d6" },
  { id: "qgd-declined", play: "d2d4,d7d5,c2c4,e7e6" },
];

async function fetchStats(play) {
  const url =
    `https://explorer.lichess.ovh/lichess?play=${play}` +
    `&speeds=blitz,rapid,classical` +
    `&ratings=1000,1200,1400,1600,1800,2000,2200,2500`;
  const res = await fetch(url, {
    headers: { "User-Agent": "cheesychess-opening-trainer/1.0 (educational)" },
  });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${play}`);
  return res.json();
}

const results = {};

for (const opening of OPENINGS) {
  try {
    const data = await fetchStats(opening.play);
    const total = data.white + data.draws + data.black;
    if (total === 0) {
      console.warn(`  ⚠ No games for ${opening.id}`);
      continue;
    }
    const w = Math.round((data.white / total) * 100);
    const d = Math.round((data.draws / total) * 100);
    const b = 100 - w - d; // avoids rounding to 101%
    results[opening.id] = { games: total, whiteWin: w, draw: d, blackWin: b };
    console.log(
      `  ${opening.id}: ${total.toLocaleString()} games  W${w}% D${d}% B${b}%`,
    );
  } catch (err) {
    console.error(`  ✗ ${opening.id}: ${err.message}`);
  }
}

const ts = `// Auto-generated by scripts/fetch-opening-stats.mjs — re-run to refresh.
// Source: Lichess opening explorer (all ratings, blitz/rapid/classical)
// Last updated: ${new Date().toISOString().slice(0, 10)}

export interface OpeningStats {
  /** Total games in the Lichess database at this position */
  games: number;
  /** White win % (0–100) */
  whiteWin: number;
  /** Draw % (0–100) */
  draw: number;
  /** Black win % (0–100) */
  blackWin: number;
}

export const OPENING_STATS: Record<string, OpeningStats> = ${JSON.stringify(results, null, 2)};
`;

writeFileSync("src/data/openingStats.ts", ts);
console.log("\nWrote src/data/openingStats.ts ✓");
